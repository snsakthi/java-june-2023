package july4;

public class OddEven {
    public static void main(String[] args) {
        int x = 11;
        int y = 20;

        if(x%2 == 0){
            System.out.println(" x is even");
        }else {
            System.out.println(" x is odd");
        }
        if (y%2 == 0){
            System.out.println(" y is even");
        }else {
            System.out.println("y is odd");
        }
    }
}
